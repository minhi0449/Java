package ch02.sec02;

/*
    날짜 : 2025.02.28 (금)
    이름 : 김민희
    내용 : 2진수, 8진수, 10진수, 16진수
 */

public class IntegerLiteralExample {

    public static void main(String[] args) {
        int var1 = 0b1011;  // 2진수
        int var2 = 0206;    // 8진수
        int var3 = 365;     // 10진수
        int var4 = 0xB3;    // 16진수

        System.out.println("var1 (2진수): " + var1);
        System.out.println("var2 (2진수): " + var2);
        System.out.println("var3 (2진수): " + var3);
        System.out.println("var4 (2진수): " + var4);

    }

}
